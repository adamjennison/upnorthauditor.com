<h1>About this site...</h1>
<p>This site has been created by me, Adam Jennison, to aid public transparency of 
<?php echo link_to('Hull City Councils','http://www.hullcc.gov.uk') ?> spend data.</p>
<p>
The Council provides its spend data in <?php echo link_to('CSV format','http://www.hullcc.gov.uk/opendata')?> which is great, we can see where every &pound; is spent (in reason) but.. Commar Seperate Value files are not good for humans.
Hence this site...
</p>
